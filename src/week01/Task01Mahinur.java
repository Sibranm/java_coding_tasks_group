public class Task01Mahinur {

    public static void main(String[] args) {
        oddOrEven(7);
        oddOrEven(8);
    }


    public static void oddOrEven(int n) {
        if (n % 2 == 0) {
            System.out.println("It is an even number");
        } else {
            System.out.println(" It is an odd number. ");

        }
    }
}



